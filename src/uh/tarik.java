    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        int a = UH.saldo;
        int b = Integer.parseInt(tfTarik.getText());
        
        if(b >= 10000){
            if(a - b > 10000){
                a -= b;
                UH.saldo = a;
                JOptionPane.showMessageDialog(null, "Anda mengambil uang sebesar Rp." + b + "\nSisa saldo anda Rp." + a,"Informasi", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"Sisal saldo minimal harus Rp.10000 setelah penarikan","PERINGATAN",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Penarikan uang minimal Rp.10000","PERINGATAN",JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_jButton12ActionPerformed

